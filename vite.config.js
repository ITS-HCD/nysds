// Externalize Lit and all NYSDS internal packages
const externalEs = (id) => id === "lit" || id.startsWith("lit/");

// Plugin to remove demo HTML files after build
function removeDemoFiles() {
  return {
    name: "remove-demo-files",
    closeBundle() {
      const demoFolder = path.resolve("dist/nys-stepper");

      if (fs.existsSync(demoFolder)) {
        fs.rmSync(demoFolder, { recursive: true, force: true });
        console.log(`✓ Removed demo directory: dist/nys-stepper/`);
      }
    },
  };
}

export function copyIconAssets() {
  return {
    name: "copy-icon-assets",
    closeBundle() {
      const srcDir = path.resolve(__dirname, "./packages/nys-icon/dist/icons");
      const destDir = path.resolve("dist/icons");

      if (!fs.existsSync(srcDir)) {
        console.warn(`⚠ Icon source not found: ${srcDir} (run nys-icon build first)`);
        return;
      }

      fs.mkdirSync(destDir, { recursive: true });
      for (const file of fs.readdirSync(srcDir)) {
        fs.copyFileSync(path.join(srcDir, file), path.join(destDir, file));
      }
      console.log(`✓ Copied icon assets to dist/icons/`);
    },
  };
}


export function extractIcons() {
  return {
    name: "extract-icons",
    closeBundle() {
      const __dirname = dirname(fileURLToPath(import.meta.url));
      const srcFile = resolve(__dirname, "./packages/nys-icon/src/nys-icon.library.ts");
      const outDir = resolve(__dirname, "./packages/nys-icon/dist/icons");

      const source = readFileSync(srcFile, "utf8");

      // Match each icon entry: iconName: `<svg...>...</svg>`,
      // Allow optional whitespace between </svg> and the closing backtick,
      // since some entries have a newline before the backtick.
      const iconRegex = /(\w+):\s*`(<svg[\s\S]*?<\/svg>)\s*`/g;
      let match;
      const names = [];

      if (!existsSync(outDir)) {
        mkdirSync(outDir, { recursive: true });
      }

      while ((match = iconRegex.exec(source)) !== null) {
        const [, name, svg] = match;
        // Clean up leading whitespace from template literal indentation
        const cleaned = svg.replace(/^\s+/gm, "").trim();
        writeFileSync(resolve(outDir, `${name}.svg`), cleaned, "utf8");
        names.push(name);
      }

      // Write manifest with sorted icon names
      writeFileSync(
        resolve(outDir, "manifest.json"),
        JSON.stringify({ icons: names.sort() }, null, 2),
        "utf8",
      );

      console.log(`Extracted ${names.length} icons to dist/icons/`);
    },
  };
}


// Shared base config — exported for component packages to extend via mergeConfig
export const defaultConfig = {
  css: {
    postcss: null,
  },
  esbuild: {
    legalComments: 'inline',
  },
  build: {
    lib: {
      entry: ["./src/index.ts"],
      fileName: () => "nysds.es.js",
      formats: ["es"],
    },
    minify: 'esbuild',
    sourcemap: true,
    emptyOutDir: false, // Preserves .d.ts files generated by tsc
    rollupOptions: {
      onwarn(warning, defaultHandler) {
        if (
          warning.code === "INVALID_ANNOTATION" &&
          warning.message.includes("@stencil/core")
        ) {
          return;
        }
        defaultHandler(warning);
      },
      external: externalEs,
      output: {
        compact: true,
        banner,
        dir: "dist",
        globals: {
          lit: "Lit",
        },
      },
      plugins: [
        shouldAnalyze &&
          visualizer({ filename: "dist/stats-es.html", open: true }),
        removeDemoFiles(),
      ].filter(Boolean),
    },
  },
};

// Root build: switch between ES and UMD via BUILD_FORMAT env var.
// UMD config is constructed explicitly (not via mergeConfig) to avoid
// array-merging pitfalls with plugins and external.
export default isUmd
  ? defineConfig({
      css: {
        postcss: null,
      },
      esbuild: {
        legalComments: 'inline',
      },
      build: {
        lib: {
          entry: ["./src/index.ts"],
          name: "NYSDS", // Global variable for UMD
          fileName: () => "nysds.js",
          formats: ["umd"],
        },
        minify: 'esbuild',
        sourcemap: true,
        emptyOutDir: false,
        rollupOptions: {
          onwarn(warning, defaultHandler) {
            if (
              warning.code === "INVALID_ANNOTATION" &&
              warning.message.includes("@stencil/core")
            ) {
              return;
            }
            defaultHandler(warning);
          },
          external: [], // Bundle all dependencies including Lit
          output: {
            compact: true,
            banner,
            globals: {
              lit: "Lit",
            },
          },
          plugins: [
            shouldAnalyze &&
              visualizer({ filename: "dist/stats-umd.html", open: true }),
            removeDemoFiles(),
            copyIconAssets(),
          ].filter(Boolean),
        },
      },
    })
  : mergeConfig(defaultConfig, {
      build: {
        rollupOptions: {
          plugins: [copyIconAssets()],
        },
      },
    });
